-- Q1: What is the difference between COUNT(*) and COUNT(column)?
-- A : COUNT(*) counts all rows, including rows with NULL values
--     in any column. COUNT(column) counts only rows where that
--     specific column is NOT NULL.

-- Q2: What does COUNT("some text") actually do?
-- A : It treats the text as a constant value, not a search
--     term. Since the constant is always non-null, it counts
--     every row — identical to COUNT(*). It does NOT filter
--     by that text. To search for a specific value, use WHERE.

-- Q3: When should you use LIKE vs = for matching text?
-- A : Use = for an exact, known match (faster, precise).
--     Use LIKE only when you need partial/pattern matching,
--     e.g., unsure of exact spelling or searching for a fragment
--     within a larger string.

-- Q4: What is the difference between SUM and AVG?
-- A : SUM adds up all values in a group (total).
--     AVG calculates the mean (total divided by count).
--     Choosing the wrong one gives a technically valid but
--     business-meaningless result — always match the function
--     to what's actually being asked.

