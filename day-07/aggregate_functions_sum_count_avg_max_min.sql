USE sql_learning;


-- ============================================================
-- STEP 1: VIEW DATA
-- ============================================================

SELECT * FROM sales_data;


-- ============================================================
-- STEP 2: SUM — Total Net Sales Per Segment
-- ============================================================

-- BUSINESS QUESTION:
-- "What is the total sales amount generated by each segment?"

SELECT
    segment,
    SUM(net_sales) AS total_sales
FROM sales_data
GROUP BY segment;


-- ============================================================
-- STEP 3: COUNT(*) — Total Orders Per Region
-- ============================================================

-- BUSINESS QUESTION:
-- "How many total orders came from each region?"

SELECT
    region,
    COUNT(*) AS total_orders
FROM sales_data
GROUP BY region;


-- ============================================================
-- STEP 4: AVG — Average Profit/Loss Per Segment
-- ============================================================

-- BUSINESS QUESTION:
-- "On average, is each segment making profit or loss?"

SELECT
    segment,
    AVG(profit_loss) AS avg_profit_loss
FROM sales_data
GROUP BY segment;


-- ============================================================
-- STEP 5: MAX and MIN Together — Profit/Loss Range Per Region
-- ============================================================

-- BUSINESS QUESTION:
-- "What is the best and worst profit/loss recorded in each
--  region? I want to see how volatile each region's
--  performance is."

SELECT
    region,
    MAX(profit_loss) AS max_profit_loss,
    MIN(profit_loss) AS min_profit_loss
FROM sales_data
GROUP BY region;


-- ============================================================
-- STEP 6: COUNT — Finding a Specific Customer (Corrected)
-- ============================================================

-- BUSINESS QUESTION:
-- "How many orders has the customer James Murray placed?"
SELECT COUNT(*) AS james_total
FROM sales_data
WHERE customer_name = 'James Murray';

-- ALTERNATE — pattern match with LIKE
-- Use LIKE only when genuine partial matching is needed
-- (e.g., unsure of exact spelling, or searching for a fragment)
SELECT COUNT(*) AS james_total
FROM sales_data
WHERE customer_name LIKE '%James Murray%';


-- ============================================================
-- STEP 7: SUM — Total Customers Served Per Segment
-- ============================================================

-- BUSINESS QUESTION:
-- "What is the total number of customers served, broken
--  down by segment?"

SELECT
    segment,
    SUM(no_customers) AS total_customers
FROM sales_data
GROUP BY segment;


-- ============================================================
-- STEP 8: AVG — Average Customers Per Order, By Region (Corrected)
-- ============================================================

-- BUSINESS QUESTION:
-- "On average, how many customers are involved per order
--  in each region?"

SELECT
    region,
    AVG(no_customers) AS avg_customers
FROM sales_data
GROUP BY region;

-- If TOTAL (not average) customers per region was actually
-- needed instead, this would be the correct version:
SELECT
    region,
    SUM(no_customers) AS total_customers
FROM sales_data
GROUP BY region;


